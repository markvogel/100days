# https://edabit.com/challenge/4afgmFpLP6CpwtRMY
def sudoku_validator(grid):
    a = np.array(grid)
    b = [(a[:3, :3]).sum(),
         (a[3:6, :3]).sum(),
         (a[6:9, :3]).sum(),
         (a[:3, 3:6]).sum(),
         (a[3:6, 3:6]).sum(),
         (a[6:9, 3:6]).sum(),
         (a[:3, 6:9]).sum(),
         (a[3:6, 6:9]).sum(),
         (a[6:9, 6:9]).sum()]
    for i in range(len(grid)):
        if a[:][i].sum() != 45:
            return False
        if a[i][:].sum() != 45:
            return False
        if a[:3][:i + 3].sum() != 45:
            return False
    for i in b:
        ff(i)


